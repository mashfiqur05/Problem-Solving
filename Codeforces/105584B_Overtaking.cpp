void testCases(int tt)
{
    while(1)
    {
        int n;
        cin >> n;
        if (n == 0) break;;
        vector<ll> a(n), b(n);
        for (int i = 0; i < n; i++)
            cin >> a[i];
        for (int i = 0; i < n; i++)
            cin >> b[i];
        vector<ll> prea(n + 1, 0), preb(n + 1, 0);
        for (int i = 1; i <= n; i++)
        {
            prea[i] = prea[i - 1] + a[i - 1];
            preb[i] = preb[i - 1] + b[i - 1];
        }

        // dbg(prea);
        // dbg(preb);
        int ans = 0;
        int f = -1;
        for (int i = 1; i <= n; i++)
        {
            if (f == -1)
            {
                if (prea[i] > preb[i]) f = 1;
                else if (prea[i] < preb[i]) f = 2;
            }
            else if (f == 1 && prea[i] < preb[i])
            {
                f = 2;
                ans++;
            }
            else if (f == 2 && prea[i] > preb[i])
            {
                f = 1;
                ans++;
            }
        }

        cout << ans << endl;
    }
}


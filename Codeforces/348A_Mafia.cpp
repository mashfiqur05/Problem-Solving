int32_t main()
{
    fastio();

    int testcases = 1;
    // cin >> testcases;
    for (int tt = 1; tt <= testcases; tt++)
    {
        ll n;
        cin >> n;
        vector<int> v(n);
        for (int i = 0; i < n; i++) cin >> v[i];

        ll sum = accumulate (all (v), 0LL);
        ll mx = *max_element (all (v));
        ll l = 0, r = 1e10, ans = 0;
        // dbg (sum);
        while (l <= r)
        {
            ll mid = l + (r - l) / 2;
            // dbg (l, r, mid);
            ll total = mid * (n-1);
            if (total > sum)
            {
                ans = mid;
                r = mid-1;
            }
            else if (total == sum)
            {
                ans = mid;
                break;
            }
            else l = mid+1;
        }

        cout << max (ans, mx) << endl;
    }

    return 0;
}
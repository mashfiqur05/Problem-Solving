// Upper bound: Max index where I can insert x still v is sorted.
int ub(const vector<int> &arr, int x)
{
    int low = 0, high = arr.size();
    while (low < high)
    {
        int mid = low + (high - low) / 2;
        if (arr[mid] <= x) low = mid + 1;
        else high = mid;    
    }

    return low;
}


void solve ()
{
    int n; cin >> n;
    map<string, int> cnt;
    for (int i = 0; i < n; i++)
    {
        string s; cin >> s;
        if (s.size() > 2) sort (s.begin()+1, s.end()-1);
        cnt[s]++;
    }

    // dbg (cnt);

    int m; cin >> m;

    cin.ignore(numeric_limits<streamsize>::max(), '\n');
    for (int i = 0; i < m; i++)
    {
        int ans = 1;
        string s;
        getline (cin, s);
        // cout << s << endl;
        string cur = "";
        for (int j = 0; j < s.size(); j++)
        {
            if (s[j] == ' ')
            {
                if (!cur.empty())
                {
                    if (cur.size() > 2)
                        sort(cur.begin() + 1, cur.end() - 1);

                    ans *= cnt[cur];
                    cur = "";
                }
            }
            else cur += s[j];
        }

        if (!cur.empty())
        {
            if (cur.size() > 2)
                sort(cur.begin() + 1, cur.end() - 1);

            ans *= cnt[cur];
        }
        cout << ans << endl;
    }
}


